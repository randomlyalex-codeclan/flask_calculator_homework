class Calculator():

    def add_two_numbers(number_1, number_2):
        return int(number_1) + int(number_2)

    def subtract_two_numbers(number_1, number_2):
        return int(number_1) - int(number_2)

    def multiply_two_numbers(number_1, number_2):
        return int(number_1) * int(number_2)

    def divide_two_numbers(number_1, number_2):
        return int(number_1) / int(number_2)