class Calculator():
    
    def add(self, number_1, number_2):
        return number_1 + number_2

    def subtract(self, number_1, number_2):
        return number_1 - number_2

    def multiply(self, number_1, number_2):
        return number_1 * number_2

    def divide(self, number_1, number_2):
        return number_1 / number_2